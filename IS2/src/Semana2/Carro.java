//atributos modelo y marca
package Semana2;

public class Carro {
    public int modelo;
    public String marca;
    
    //crear sus metodos setter y getter

    public Carro() {
    }

    public int getModelo() {
        return modelo;
    }

    public void setModelo(int modelo) {
        this.modelo = modelo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }
    
}
